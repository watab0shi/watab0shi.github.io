//---------------------------------------------------------------------------------------------------- mouseClick
function mouseClick(){
  if( ( selX != null ) && ( selY != null ) ){
    var isOdd = ( wcY % 2 == 1 ) ? true: false;
    var cy    = -scrollTop + ( wcY * ( HEX_MAX_RADIUS * 1.5 ) );
    var cx    = ( isOdd ) ? ( wcX * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( wcX * ( HEX_MAX_IN_RADIUS * 2 ) );
    var r     = HEX_MAX_RADIUS * 3.1;
    var a     = ( Math.PI * 2 ) / 3;
    var x     = new Array();
    var y     = new Array();
    for( var i = 0; i < 3; ++i ){
      x[ i ] = cx + ( Math.cos( ( i * a ) - Math.PI / 2 ) * r );
      y[ i ] = cy + ( Math.sin( ( i * a ) - Math.PI / 2 ) * r );

      drawHexagon( x[ i ], y[ i ], selRadius );

      if( ctx.isPointInPath( mouseX, mouseY ) ){
        if( i == 0 ){
          selX      = null;
          selY      = null;
          selRadius = 0.0;
        }
        return;
      }
    }
    selX      = null;
    selY      = null;
    selRadius = 0.0;
    return;
  }

  var wIdx = 0;
  for( var y = 0; y < HEX_BG_PALLET.length; ++y ){
    var isOdd = ( y % 2 == 1 ) ? true: false;
    var py    = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );

    for( var x = 0; x < HEX_BG_PALLET[ y ].length; ++x ){
      var px = ( isOdd ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );

      if( HEX_WORKS[ y ][ x ] == 1 ){
        drawHexagon( px, py, radius[ y ] );

        if( ctx.isPointInPath( mouseX, mouseY ) ){
          selX = x;
          selY = y;
          return;
        }

        ++wIdx;
      }
    }
  }

  selX      = null;
  selY      = null;
  selRadius = 0.0;
}


//---------------------------------------------------------------------------------------------------- draw
function draw(){
  requestAnimationFrame( draw );

	ctx.globalAlpha = 1.0;
	ctx.fillStyle   = "rgb( 255, 255, 255 )";
	ctx.fillRect( 0, 0, cw, ch );

	// ctx.strokeStyle = "rgb( 255, 255, 255 )";

  for( var y = 0; y < HEX_BG_PALLET.length; ++y ){
    var isOdd = ( y % 2 == 1 ) ? true: false;
    var py    = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );

    if( ( py >= -( HEX_MAX_RADIUS ) ) && ( py <= ch + HEX_MAX_RADIUS ) ){
      if( radius[ y ] > HEX_MAX_RADIUS - 0.1 ){
        radius[ y ] = HEX_MAX_RADIUS;
      }
      else{
        radius[ y ] += ( HEX_MAX_RADIUS - radius[ y ] ) / 12;
      }
    }
    else{
      if( radius[ y ] < 0.1 ){
        radius[ y ] = 0.0;
      }
      else{
        radius[ y ] += ( 0.0 - radius[ y ] ) / 12;
      }
    }

    for( var x = 0; x < HEX_BG_PALLET[ y ].length; ++x ){
      var px = ( isOdd ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );

      // bg
      ctx.fillStyle   = HEX_BG_PALLET[ y ][ x ];
      ctx.lineWidth   = 0.0;
      ctx.globalAlpha = 1.0;
      drawHexagon( px, py, radius[ y ] );

      // add
      if( HEX_ADD_PALLET[ y ][ x ] !== "rgba(0,0,0,0.00)" ){
        ctx.fillStyle   = HEX_ADD_PALLET[ y ][ x ];
        drawHexagon( px, py, radius[ y ] * 0.7 );
      }
    }
  }

  // works
  for( var y = 0; y < HEX_BG_PALLET.length; ++y ){
    var isOdd = ( y % 2 == 1 ) ? true: false;
    var py    = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );

    for( var x = 0; x < HEX_BG_PALLET[ y ].length; ++x ){
      var px = ( isOdd ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );

      if( HEX_WORKS[ y ][ x ] == 1 ){
        ctx.fillStyle   = HEX_BG_PALLET[ y ][ x ];
        ctx.strokeStyle = "rgb( 255, 255, 255 )";
        ctx.lineWidth   = HEX_MAX_RADIUS / 6;
        drawImageHexagon( px, py, radius[ y ], images[ wIdx[ y ][ x ] % 4 ] );
      }
    }
  }

  // select
  if( ( selX!= null ) && ( selY != null ) ){

    var isOdd = ( selY % 2 == 1 ) ? true: false;
    var py    = -scrollTop + ( selY * ( HEX_MAX_RADIUS * 1.5 ) );
    var px    = ( isOdd ) ? ( selX * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( selX * ( HEX_MAX_IN_RADIUS * 2 ) );
    ctx.fillStyle   = HEX_BG_PALLET[ selY ][ selX ];
    ctx.strokeStyle = "rgb( 255, 255, 255 )";
    ctx.lineWidth   = HEX_MAX_RADIUS / 6;
    drawImageHexagon( px, py, radius[ selY ], images[ wIdx[ selY ][ selX ] % 4 ] );

    ctx.fillStyle = "rgba( 0, 0, 0, 0.3 )";
    ctx.fillRect( 0, 0, cw, ch );

    if( selRadius > ( ( HEX_MAX_RADIUS * 3 ) - 0.1 ) ){
      selRadius = HEX_MAX_RADIUS * 3;
    }
    else{
      selRadius += ( ( HEX_MAX_RADIUS * 3 ) - selRadius ) / 12;
    }

    var isOdd = ( wcY % 2 == 1 ) ? true: false;
    var cy    = -scrollTop + ( wcY * ( HEX_MAX_RADIUS * 1.5 ) );
    var cx    = ( isOdd ) ? ( wcX * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( wcX * ( HEX_MAX_IN_RADIUS * 2 ) );

    var r  = HEX_MAX_RADIUS * 3.1;
    var _r = selRadius;
    var a  = ( Math.PI * 2 ) / 3;
    var x = new Array();
    var y = new Array();
    for( var i = 0; i < 3; ++i ){
      x[ i ] = cx + ( Math.cos( ( i * a ) - Math.PI / 2 ) * r );
      y[ i ] = cy + ( Math.sin( ( i * a ) - Math.PI / 2 ) * r );
    }

    var i = images[ wIdx[ selY ][ selX ] % 4 ];


    // image
    ctx.save();
    ctx.translate( x[ 0 ], y[ 0 ] );
    drawHexPolygon( _r );
    ctx.clip();
    ctx.drawImage( i, 0, 0, img.width, img.height, -_r, -_r, _r * 2, _r * 2 );
    ctx.restore();

    // right
    ctx.fillStyle = "rgb( 255, 255, 255 )";
    drawHexagon( x[ 1 ], y[ 1 ], _r );

    //left
    drawHexagon( x[ 2 ], y[ 2 ], _r );
  }
}

//---------------------------------------------------------------------------------------------------- drawImageHexagon
function drawImageHexagon( _x, _y, _r, _i ){
  ctx.save();

  ctx.translate( _x, _y );
  drawHexPolygon( _r );
  ctx.fill();
  ctx.stroke();

  if( ( selX == null ) && ( selY == null ) ){
    if( ctx.isPointInPath( mouseX, mouseY ) ){
      ctx.clip();
      ctx.drawImage( _i, 0, 0, _i.width, _i.height, -_r, -_r, _r * 2, _r * 2 );
      ctx.stroke();
    }
  }

  ctx.restore();
}


//---------------------------------------------------------------------------------------------------- drawHexagon
function drawHexagon( _x, _y, _r ){
  ctx.save();
  ctx.translate( _x, _y );

  drawHexPolygon( _r );
  ctx.fill();

  ctx.restore();
}


//---------------------------------------------------------------------------------------------------- drawHexPolygon
function drawHexPolygon( _r ){
	var angle  = ( ( Math.PI * 2 ) / HEX_NUM_ANGLES );

	ctx.beginPath();
	for( var i = 0; i <= HEX_NUM_ANGLES; ++i ){
		var a = ( i + 0.5 ) * angle;
		var x = Math.cos( a ) * _r,
		    y = Math.sin( a ) * _r;

		if( i == 0 ){
			ctx.moveTo( x, y );
		}
		else{
			ctx.lineTo( x, y );
		}
	}
	ctx.closePath();
}

//---------------------------------------------------------------------------------------------------- mouseClick
function mouseClick(){
  // popup
  if( selIdx != null ){
    if( enableClick ){
      // close button
      var btnX = cw - HEX_MAX_RADIUS * 1.5;
      var btnY = HEX_MAX_RADIUS;
      var lr   = HEX_MAX_IN_RADIUS * 0.5;
      ctx.beginPath();
      ctx.arc( btnX, btnY, lr, 0, Math.PI * 2, false );
      ctx.closePath();

      if( ctx.isPointInPath( mouseX, mouseY ) ){
        selIdx    = null;
        selRadius = 0.0;

        // resume scroll
        $( '#wrapper' ).removeClass( 'noScroll' ).css( 'top', 0 );
        $( document ).scrollTop( scrollTop );

        $( '.left' ).css( 'display', 'none' );
        $( '.right' ).css( 'display', 'none' );
        return;
      }

      // left arrow
      if( selIdx > 0 ){
        ctx.beginPath();
        ctx.arc( HEX_MAX_RADIUS * 1.2, hh, lr * 2.0, 0, Math.PI * 2, false );
        ctx.closePath();
        if( ctx.isPointInPath( mouseX, mouseY ) ){
          selRadius = 0.0;
          selEaseStartTime = new Date().getTime();
          --selIdx;

          enableClick = false;

          var work = '.grade_' + ( works[ selIdx ].grade ) + '_' + ( works[ selIdx ].index );

          $( '.left' ).css( 'display', 'none' );
          $( '.right' ).css( 'display', 'none' );
          $( work + '>.left' ).delay( 400 ).fadeIn( 'slow' );
          $( work + '>.right' ).delay( 400 ).fadeIn( 'slow', function(){
            enableClick = true;
          } );
        }
      }
      // right arrow
      if( selIdx < ( works.length - 1 ) ){
        ctx.beginPath();
        ctx.arc( cw - ( HEX_MAX_RADIUS * 1.2 ), hh, lr * 2.0, 0, Math.PI * 2, false );
        ctx.closePath();
        if( ctx.isPointInPath( mouseX, mouseY ) ){
          selRadius = 0.0;
          selEaseStartTime = new Date().getTime();
          ++selIdx;

          enableClick = false;

          var work = '.grade_' + ( works[ selIdx ].grade ) + '_' + ( works[ selIdx ].index );

          $( '.left' ).css( 'display', 'none' );
          $( '.right' ).css( 'display', 'none' );
          $( work + '>.left' ).delay( 400 ).fadeIn( 'slow' );
          $( work + '>.right' ).delay( 400 ).fadeIn( 'slow', function(){
            enableClick = true;
          } );
        }
      }

    }
    return;
  }

  // select
  var bSelect = false;
  for( var i = 0; i < works.length; ++i ){
    var y  = works[ i ].y;
    var x  = works[ i ].x;
    var py = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );
    var px = ( y % 2 == 1 ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );

    ctx.save();
    ctx.translate( px, py );
    drawHexPolygon( ctx, radius[ y ] - 0.5 );
    if( !bSelect && ctx.isPointInPath( mouseX, mouseY ) ){
      selIdx           = i;
      bSelect          = true;
      selRadius        = 0.0;
      selEaseStartTime = new Date().getTime();

      enableClick = false;

      // disable scroll
      $( '#wrapper' ).addClass( 'noScroll' ).css( 'top', -scrollTop );

      var work = '.grade_' + ( works[ selIdx ].grade ) + '_' + ( works[ selIdx ].index );

      $( work + '>.left' ).delay( 400 ).fadeIn( 'slow' );
      $( work + '>.right' ).delay( 400 ).fadeIn( 'slow', function(){
        enableClick = true;
      } );
    }
    ctx.restore();
  }

  if( !bSelect ){
    selIdx = null;
  }
}


//---------------------------------------------------------------------------------------------------- draw
function draw(){
  requestAnimationFrame( draw );

	ctx.globalAlpha = 1.0;
	ctx.fillStyle   = "rgb( 255, 255, 255 )";
	ctx.fillRect( 0, 0, cw, ch );

  for( var y = 0; y < HEX_BG_PALLET.length; ++y ){
    var py = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );

    // update radius with easing
    if( ( py >= -( HEX_MAX_RADIUS ) ) && ( py <= ch + HEX_MAX_RADIUS ) ){
      easeTime[ y ] = new Date().getTime() - easeStartTime[ y ];
      var e = easeOutQuart( easeTime[ y ], easeLength, 1.0, 0.0 );
      if( easeTime[ y ] >= easeLength ){
        radius[ y ] = HEX_MAX_RADIUS;
      }
      else{
        radius[ y ] = e * HEX_MAX_RADIUS;
      }
    }
    else{
      easeStartTime[ y ] = new Date().getTime();
      radius[ y ] = 0.0;
    }

    // draw hexagons
    for( var x = 0; x < HEX_BG_PALLET[ y ].length; ++x ){
      if( ( HEX_BG_PALLET[ y ][ x ] == "rgb(255,255,255)" ) || ( HEX_WORKS[ y ][ x ] == 1 ) ){
        continue;
      }

      var px = ( y % 2 == 1 ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );

      // bg
      ctx.fillStyle   = HEX_BG_PALLET[ y ][ x ];
      ctx.lineWidth   = 0.0;
      drawHexagon( ctx, px, py, radius[ y ] );

      // add
      if( HEX_ADD_PALLET[ y ][ x ] !== "rgba(0,0,0,0.00)" ){
        ctx.fillStyle   = HEX_ADD_PALLET[ y ][ x ];
        drawHexagon( ctx, px, py, radius[ y ] * 0.7 );
      }
    }
  }

  // works
  // set curcor default
  $( 'body' ).css( 'cursor', 'default' );

  ctx.strokeStyle = "rgb( 255, 255, 255 )";
  ctx.lineWidth   = HEX_MAX_RADIUS / 6;
  for( var i = 0; i < works.length; ++i ){
    var y  = works[ i ].y;
    var x  = works[ i ].x;
    var py = -scrollTop + ( y * ( HEX_MAX_RADIUS * 1.5 ) );
    var px = ( y % 2 == 1 ) ? ( x * ( HEX_MAX_IN_RADIUS * 2 ) ): HEX_MAX_IN_RADIUS + ( x * ( HEX_MAX_IN_RADIUS * 2 ) );
    ctx.fillStyle = HEX_BG_PALLET[ y ][ x ];
    drawImageHexagon( px, py, radius[ y ], images[ i ], ( i == selIdx ) );
  }

  // select
  sCtx.clearRect( 0, 0, cw, ch );

  if( selIdx != null ){
    $( 'body' ).css( 'cursor', 'default' );

    sCtx.fillStyle = "rgba( 0, 0, 0, 0.6 )";
    sCtx.fillRect( 0, 0, cw, ch );

    // update select radius with easing
    selEaseTime = new Date().getTime() - selEaseStartTime;
    var e = easeOutQuart( selEaseTime, selEaseLength, 1.0, 0.0 );
    if( selEaseTime >= selEaseLength ){
      selRadius = SEL_MAX_RADIUS;
    }
    else{
      selRadius = e * SEL_MAX_RADIUS;
    }

    // center of window
    var cy = hh + HEX_MAX_RADIUS * 0.77;
    var cx = hw;

    var r  = SEL_MAX_RADIUS + HEX_MAX_RADIUS * 0.1;
    var _r = selRadius;
    // _r = SEL_MAX_RADIUS;
    var a  = ( Math.PI * 2 ) / 3;
    var x  = [];
    var y  = [];
    for( var i = 0; i < 3; ++i ){
      x[ i ] = cx + ( Math.cos( ( i * a ) - Math.PI / 2 ) * r );
      y[ i ] = cy + ( Math.sin( ( i * a ) - Math.PI / 2 ) * r );
    }

    var i = images[ selIdx ];
    sCtx.globalAlpha = selRadius;
    // image
    sCtx.save();
    sCtx.translate( x[ 0 ], y[ 0 ] );
    drawHexPolygon( sCtx, _r );
    sCtx.clip();
    sCtx.drawImage( i, 0, 0, i.width, i.height, -_r, -_r, _r * 2, _r * 2 );
    sCtx.restore();

    // right
    sCtx.fillStyle = "rgb( 255, 255, 255 )";
    drawHexagon( sCtx, x[ 1 ], y[ 1 ], _r );

    //left
    drawHexagon( sCtx, x[ 2 ], y[ 2 ], _r );

    // close button
    var btnX = cw - HEX_MAX_RADIUS * 1.5;
    var btnY = HEX_MAX_RADIUS;
    var lr   = HEX_MAX_IN_RADIUS * 0.5;
    sCtx.beginPath();
    sCtx.arc( btnX, btnY, lr, 0, Math.PI * 2, false );
    sCtx.closePath();

    // hover
    if( sCtx.isPointInPath( mouseX, mouseY ) ){
      $( 'body' ).css( 'cursor', 'pointer' );
    }

    sCtx.lineWidth = ( sCtx.isPointInPath( mouseX, mouseY ) ) ? 2.5: 1;
    sCtx.strokeStyle = "rgb( 255, 255, 255 )";
    drawCross( btnX, btnY, lr );
    sCtx.lineWidth = 1;

    // left arrow
    if( selIdx > 0 ){
      sCtx.beginPath();
      sCtx.arc( HEX_MAX_RADIUS * 1.2, hh, lr * 2.0, 0, Math.PI * 2, false );
      sCtx.closePath();
      sCtx.lineWidth = ( sCtx.isPointInPath( mouseX, mouseY ) ) ? 2.5: 1;

      // hover
      if( sCtx.isPointInPath( mouseX, mouseY ) ){
        $( 'body' ).css( 'cursor', 'pointer' );
      }

      drawArrow( HEX_MAX_RADIUS, hh, lr * 1.5, true );
    }

    // right arrow
    if( selIdx < ( works.length - 1 ) ){
      sCtx.beginPath();
      sCtx.arc( cw - HEX_MAX_RADIUS * 1.2, hh, lr * 2.0, 0, Math.PI * 2, false );
      sCtx.closePath();
      sCtx.lineWidth = ( sCtx.isPointInPath( mouseX, mouseY ) ) ? 2.5: 1;

      // hover
      if( sCtx.isPointInPath( mouseX, mouseY ) ){
        $( 'body' ).css( 'cursor', 'pointer' );
      }

      drawArrow( cw - HEX_MAX_RADIUS, hh, lr * 1.5, false );
      sCtx.lineWidth = 1;
    }
  }
}

//---------------------------------------------------------------------------------------------------- drawImageHexagon
function drawImageHexagon( _x, _y, _r, _i, _s ){
  ctx.save();

  ctx.translate( _x, _y );
  drawHexPolygon( ctx, _r );
  ctx.fill();
  ctx.stroke();

  if( _s ){
    drawHexPolygon( ctx, _r - ctx.lineWidth / 2 );
    ctx.clip();
    ctx.drawImage( _i, 0, 0, _i.width, _i.height, -_r, -_r, _r * 2, _r * 2 );
  }
  else{
    if( selIdx == null ){
      drawHexPolygon( ctx, _r - 0.5 );
      if( ctx.isPointInPath( mouseX, mouseY ) ){
        drawHexPolygon( ctx, _r - ctx.lineWidth / 2 );
        ctx.clip();
        ctx.drawImage( _i, 0, 0, _i.width, _i.height, -_r, -_r, _r * 2, _r * 2 );

        $( 'body' ).css( 'cursor', 'pointer' );
      }
    }
  }

  ctx.restore();
}


//---------------------------------------------------------------------------------------------------- drawHexagon
function drawHexagon( _ctx, _x, _y, _r ){
  _ctx.save();
  _ctx.translate( _x, _y );

  drawHexPolygon( _ctx, _r );
  _ctx.fill();

  _ctx.restore();
}


//---------------------------------------------------------------------------------------------------- drawHexPolygon
function drawHexPolygon( _ctx, _r ){
	var angle  = ( ( Math.PI * 2 ) / HEX_NUM_ANGLES );

	_ctx.beginPath();
	for( var i = 0; i <= HEX_NUM_ANGLES; ++i ){
		var a = ( i + 0.5 ) * angle;
		var x = Math.cos( a ) * _r,
		    y = Math.sin( a ) * _r;

		if( i == 0 ){
			_ctx.moveTo( x, y );
		}
		else{
			_ctx.lineTo( x, y );
		}
	}
	_ctx.closePath();
}


//---------------------------------------------------------------------------------------------------- drawCross
function drawCross( _x, _y, _r ){
  sCtx.save();
  sCtx.translate( _x, _y );

  sCtx.beginPath();
  sCtx.moveTo( Math.cos( Math.PI * 1.25 ) * _r, Math.sin( Math.PI * 1.25 ) * _r );
  sCtx.lineTo( Math.cos( Math.PI * 0.25 ) * _r, Math.sin( Math.PI * 0.25 ) * _r );
  sCtx.closePath();
  sCtx.stroke();

  sCtx.beginPath();
  sCtx.moveTo( Math.cos( Math.PI * 1.75 ) * _r, Math.sin( Math.PI * 1.75 ) * _r );
  sCtx.lineTo( Math.cos( Math.PI * 0.75 ) * _r, Math.sin( Math.PI * 0.75 ) * _r );
  sCtx.closePath();
  sCtx.stroke();

  sCtx.restore();
}


//---------------------------------------------------------------------------------------------------- drawCross
function drawArrow( _x, _y, _r, _l ){
  sCtx.save();
  sCtx.translate( _x, _y );
  sCtx.scale( ( _l ) ? 1: -1, 1 );

  sCtx.beginPath();
  sCtx.moveTo( 0, 0 );
  sCtx.lineTo( Math.cos( -( Math.PI / 3 ) ) * _r, Math.sin( -( Math.PI / 3 ) ) * _r );
  sCtx.closePath();
  sCtx.stroke();

  sCtx.beginPath();
  sCtx.moveTo( 0, 0 );
  sCtx.lineTo( Math.cos( Math.PI / 3 ) * _r, Math.sin( Math.PI / 3 ) * _r );
  sCtx.closePath();
  sCtx.stroke();

  sCtx.restore();
}


//---------------------------------------------------------------------------------------------------- easeOutQuart
function easeOutQuart( t, total, max, min ){
  max -= min;
  t    = ( t / total ) - 1;
  return -max * ( ( t * t * t * t ) - 1 ) + min;
}
